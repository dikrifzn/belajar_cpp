#include <iostream>
using namespace std;

int main(){
	cout<<"===PROGRAM INCREMENT & DECREMENT==="<<endl;
	int a,b;
	
	cout<<"Masukan Nilai yg akan di increment : ";
	cin>>a;
	
	cout<<"Nilai awal = "<<a<<endl;
	cout<<"Nilai increment ++a = "<<++a<<endl;
	cout<<"Nilai increment a++ = "<<a++<<endl;
	
	cout<<"Masukan Nilai yg akan di decrement : ";
	cin>>a;
	
	cout<<"Nilai awal = "<<a<<endl;
	cout<<"Nilai increment --a = "<<--a<<endl;
	cout<<"Nilai increment a-- = "<<a--<<endl;
}

